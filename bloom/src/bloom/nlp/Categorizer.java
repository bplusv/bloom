    public static int bloomCategorize(String verbStem) {
        int bloomCategory = -1;
        for (int i = 0; i < bloomTaxonomy.length; i++) {
            for (int j = 0; j < bloomTaxonomy[i].length; j++) {
                if (verbStem.equals(NLPTools.stemVerb(bloomTaxonomy[i][j]))) {
                    bloomCategory = i;
                }
            }
        }
        return bloomCategory;
    }
    
